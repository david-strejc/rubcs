static const char* fontVertSrc = R"(
#version 330 core
layout(location = 0) in vec4 vertex; // xy = pos, zw = texcoord
uniform mat4 projection;
out vec2 TexCoord;
void main() {
    gl_Position = projection * vec4(vertex.xy, 0.0, 1.0);
    TexCoord = vertex.zw;
}
)";

static const char* fontFragSrc = R"(
#version 330 core
in vec2 TexCoord;
uniform sampler2D fontTex;
uniform vec3 textColor;
out vec4 FragColor;
void main() {
    float a = texture(fontTex, TexCoord).r;
    if (a < 0.5) discard;
    FragColor = vec4(textColor, a);
}
)";

void Font::init() {
    // Create font texture atlas: 16x6 grid of 8x8 chars = 128x48 pixels
    // Characters 32-126 = 95 chars, fits in 16x6
    const int ATLAS_W = 128; // 16 chars * 8 pixels
    const int ATLAS_H = 48;  // 6 rows * 8 pixels
    unsigned char atlas[ATLAS_W * ATLAS_H];
    memset(atlas, 0, sizeof(atlas));

    for (int ch = 32; ch <= 126; ch++) {
        int idx = ch - 32;
        int cx = (idx % 16) * 8;
        int cy = (idx / 16) * 8;
        for (int row = 0; row < 8; row++) {
            unsigned char bits = font8x8[idx][row];
            for (int col = 0; col < 8; col++) {
                if (bits & (1u << col)) {
                    atlas[(cy + row) * ATLAS_W + cx + col] = 255;
                }
            }
        }
    }

    glGenTextures(1, &texture_);
    glBindTexture(GL_TEXTURE_2D, texture_);
    glTexImage2D(GL_TEXTURE_2D, 0, GL_RED, ATLAS_W, ATLAS_H, 0, GL_RED, GL_UNSIGNED_BYTE, atlas);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_NEAREST);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_NEAREST);

    // Compile shader
    GLuint vs = glCreateShader(GL_VERTEX_SHADER);
    glShaderSource(vs, 1, &fontVertSrc, nullptr);
    glCompileShader(vs);

    GLuint fs = glCreateShader(GL_FRAGMENT_SHADER);
    glShaderSource(fs, 1, &fontFragSrc, nullptr);
    glCompileShader(fs);

    shader_ = glCreateProgram();
    glAttachShader(shader_, vs);
    glAttachShader(shader_, fs);
    glLinkProgram(shader_);
    glDeleteShader(vs);
    glDeleteShader(fs);

    glGenVertexArrays(1, &vao_);
    glGenBuffers(1, &vbo_);
    glBindVertexArray(vao_);
    glBindBuffer(GL_ARRAY_BUFFER, vbo_);
    glBufferData(GL_ARRAY_BUFFER, sizeof(float) * 6 * 4 * 128, nullptr, GL_DYNAMIC_DRAW);
    glVertexAttribPointer(0, 4, GL_FLOAT, GL_FALSE, 4 * sizeof(float), 0);
    glEnableVertexAttribArray(0);
    glBindVertexArray(0);
}

void Font::cleanup() {
    glDeleteTextures(1, &texture_);
    glDeleteBuffers(1, &vbo_);
    glDeleteVertexArrays(1, &vao_);
    glDeleteProgram(shader_);
}

void Font::renderText(const std::string& text, float x, float y, float scale,
                      const glm::vec3& color, int screenW, int screenH) {
    glEnable(GL_BLEND);
    glBlendFunc(GL_SRC_ALPHA, GL_ONE_MINUS_SRC_ALPHA);
    glDisable(GL_DEPTH_TEST);

    glUseProgram(shader_);
    glm::mat4 proj = glm::ortho(0.0f, (float)screenW, 0.0f, (float)screenH);
    glUniformMatrix4fv(glGetUniformLocation(shader_, "projection"), 1, GL_FALSE, glm::value_ptr(proj));
    glUniform3fv(glGetUniformLocation(shader_, "textColor"), 1, glm::value_ptr(color));

    glActiveTexture(GL_TEXTURE0);
    glBindTexture(GL_TEXTURE_2D, texture_);

    std::vector<float> vertices;
    float charW = 8.0f * scale;
    float charH = 8.0f * scale;

    float cx = x;
    for (char c : text) {
        if (c < 32 || c > 126) { cx += charW; continue; }
        int idx = c - 32;
        int tx = idx % 16;
        int ty = idx / 16;

        float u0 = tx * 8.0f / 128.0f;
        float u1 = (tx + 1) * 8.0f / 128.0f;
        // Atlas row 0 = OpenGL v=0 (bottom). Font row 0 (top of glyph) at lower v.
        // Top of glyph: v = ty*8/48, Bottom of glyph: v = (ty+1)*8/48
        float vTop = ty * 8.0f / 48.0f;         // top of glyph (lower V in GL)
        float vBot = (ty + 1) * 8.0f / 48.0f;   // bottom of glyph (higher V in GL)

        float xpos = cx;
        float ypos = y;

        // Two triangles per character
        // Top of quad shows top of glyph (vTop), bottom of quad shows bottom of glyph (vBot)
        float quad[] = {
            xpos,          ypos + charH, u0, vTop,
            xpos,          ypos,         u0, vBot,
            xpos + charW,  ypos,         u1, vBot,

            xpos,          ypos + charH, u0, vTop,
            xpos + charW,  ypos,         u1, vBot,
            xpos + charW,  ypos + charH, u1, vTop,
        };
        for (float f : quad) vertices.push_back(f);
        cx += charW;
    }

    glBindVertexArray(vao_);
    glBindBuffer(GL_ARRAY_BUFFER, vbo_);
    glBufferSubData(GL_ARRAY_BUFFER, 0, vertices.size() * sizeof(float), vertices.data());
    glDrawArrays(GL_TRIANGLES, 0, (GLsizei)(vertices.size() / 4));

    glBindVertexArray(0);
    glDisable(GL_BLEND);
    glEnable(GL_DEPTH_TEST);
}
